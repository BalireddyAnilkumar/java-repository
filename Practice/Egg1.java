package Practice;

public class Egg1 {
    public static void printPattern(String pattern) {
        for (int i = 1; i <= 1; i++) {
            System.out.println(pattern);
        }
    }

    public static void main(String args[]) {
        printPattern("   _________");
        printPattern("  /         \\");
        printPattern(" /           \\");
        printPattern(" -\"-'-\"-'-\"-'-");
        printPattern(" \\           /");
        printPattern("  \\_________/");
        
    }
}
